#https://leetcode.com/problems/number-of-islands/description/
from typing import List
from collections import deque
class Solution:
    def numIslands(self, grid: List[List[str]]) -> int:
        rows, cols = len(grid), len(grid[0])
        if rows == cols == 1:
            ## No need to safe parse because it is either "0" or "1"
            return int(grid[0][0])
        directions = [(-1, 0), (1, 0), (0, 1), (0, -1)]
        def dfs (x, y):
            grid [x][y] = ""
            for d in directions:
                _nr, _nc = x + d[0], y + d[1]
                if 0 <= _nr < rows and 0 <= _nc < cols and grid[_nr][_nc] == "1":
                    dfs (_nr, _nc)

        islands = 0
        for r in range (rows):
            for c in range (cols):
                if grid[r][c] == "1":
                    ##NOTE: the initial implementation, which timed out, used queue beyond this point making traversal analogous to BFS. Although it worked, it visited the same cell multiple times
                    ## this is a problem of finding connected components, hence DFS
                    dfs(r, c)
                    islands += 1
        return islands

